package darkmotorsport.tunedbylog.gui.model;

public class AlvoSondaLambda {

	private Float valorMap;

	private Float valorLambda;

	public AlvoSondaLambda(Float valorMap, Float valorLambda) {
		super();
		this.valorMap = valorMap;
		this.valorLambda = valorLambda;
	}

	public Float getValorMap() {
		return valorMap;
	}

	public void setValorMap(Float valorMap) {
		this.valorMap = valorMap;
	}

	public Float getValorLambda() {
		return valorLambda;
	}

	public void setValorLambda(Float valorLambda) {
		this.valorLambda = valorLambda;
	}

}
