package darkmotorsport.tunedbylog.xml.itens;

public class Tag {
	private String nome;
	private float posicao;

	public String getNome() {
		return this.nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public float getPosicao() {
		return this.posicao;
	}

	public void setPosicao(float posicao) {
		this.posicao = posicao;
	}
}
